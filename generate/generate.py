from docx import Document
from docx.shared import Pt
from docx.oxml.ns import qn
import os

# Fungsi untuk mengisi template
def generate_cv(data, template_path, base_output_path):
    """
    Mengisi template dokumen dengan data dan menyimpannya di folder berdasarkan program studi.
    """
    # Buka template dokumen
    doc = Document(template_path)

    # Cari dan ganti placeholder di luar tabel
    for paragraph in doc.paragraphs:
        for key, value in data.items():
            # Mengganti {{Nama_Besar}} dengan format besar di bagian pertama
            if key == "Nama_Besar" and f"{{{{Nama_Besar}}}}" in paragraph.text:
                for run in paragraph.runs:
                    if f"{{{{Nama_Besar}}}}" in run.text:
                        run.font.size = Pt(18)
                        run.font.name = 'Arial'
                        run._element.rPr.rFonts.set(qn('w:eastAsia'), 'Arial')
                        run.text = run.text.replace(f"{{{{Nama_Besar}}}}", value)
            # Ganti placeholder lainnya termasuk {{Nama}} yang tidak diformat besar
            elif f"{{{{{key}}}}}" in paragraph.text:
                paragraph.text = paragraph.text.replace(f"{{{{{key}}}}}", value)

    # Cari dan ganti placeholder di dalam tabel
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for key, value in data.items():
                    if f"{{{{{key}}}}}" in cell.text:
                        cell.text = cell.text.replace(f"{{{{{key}}}}}", value)

    # Tentukan folder berdasarkan program studi
    program_studi = data.get("Program Studi", "Umum")
    output_folder = os.path.join(base_output_path, program_studi)
    
    # Buat folder jika belum ada
    os.makedirs(output_folder, exist_ok=True)
    
    # Tentukan path file output
    output_path = os.path.join(output_folder, f"Perjanjian_Kesediaan_{data['Nama'].replace(' ', '_').replace('.', '')}.docx")
    
    # Simpan dokumen yang sudah diisi
    doc.save(output_path)
    print(f"File berhasil disimpan di: {output_path}")

